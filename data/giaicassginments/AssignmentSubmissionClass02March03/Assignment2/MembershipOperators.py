
#Membership Operators

numbers = [1, 2, 3, 4, 5]
print(3 in numbers)    # True (3 is in the list)
print(10 in numbers)   # False (10 is not in the list)
print(7 not in numbers)  # True (7 is not in the list)

text = "hello"
print("h" in text)  # True ('h' is in "hello")
print("z" not in text)  # True ('z' is not in "hello")