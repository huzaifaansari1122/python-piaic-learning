#Arithmetic Operators

# Addition
a = 10
b = 5
print(a + b)  # 10 + 5 = 15

# Subtraction
print(a - b)  # 10 - 5 = 5

# Multiplication
print(a * b)  # 10 * 5 = 50

# Division
print(a / b)  # 10 / 5 = 2.0 (returns a float)

# Floor Division (removes decimal part)
print(a // b)  # 10 // 5 = 2

# Modulus (remainder of division)
print(a % b)  # 10 % 5 = 0 (no remainder)

# Exponentiation (power)
print(a ** b)  # 10^5 = 100000